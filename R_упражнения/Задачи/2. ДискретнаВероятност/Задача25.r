# Задача 25. Батерия, произведена в даден завод, е дефектна с вероятност 0.03. 
# Случайно избрани батерии се взети за проверка.
# а) Каква е вероятността да се проверят не повече от 10 батерии докато се открие първата дефектна?
# б) Каква е вероятността измежду първите 50 проверени батерии да има поне 2 дефектни?

# а)
pgeom(10-1, 0.03)

# б)
# X - броя на дефектните, от нас се търси X >= 2
# Това реално е равно на 1 - P(X >= 1)
1 - pbiom(1, 50, 0.03)