# H0 p = 1/2
# H1 p > 1/2

n < 100 # общ брой хвърляние
x <- 61 # броя хвърляния на ези
p < 1/2 # търсената от нас вероятност

# Търсене на z стойност
z.stoinost <- (x/n - p) / sqrt(p * (1-p)/n) 

# Използваме тази формула понеже H1 > H0
p.value <- 1 - pnorm(z.stoinost)

print(p.value)

# p.value = 0.139 < 0.5 
