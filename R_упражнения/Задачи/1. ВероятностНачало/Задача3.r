# Задача 3. Иван има 4 ключа, но не знае кой е за неговата стая.
# Предполагаме, че ключовете са разбъркани по произволен начин. Иван пробва последователно с всеки от тях, като помни кой ключ е пробвал.
# Каква е вероятността да отключи с последния (четвъртия) ключ?

sample_keyTake <- function() {
    takes <- sample(c(1:4), 4, replace=F)
    takes[4] == 4;
}

prob_keyTake <- function(Nrep) {
    result <- replicate(Nrep, sample_keyTake())
    sum(result) / Nrep
}