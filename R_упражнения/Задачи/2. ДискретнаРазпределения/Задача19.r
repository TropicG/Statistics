# Задача 19. Хвърляме зар, докато се падне шестица.
# а) Каква е вероятността да хвърляме не повече от 10 пъти?
# б) Каква е вероятността да хвърляме поне 6 пъти?

# а)
# Търси се P(X <= 10), където X е броят опити преди първият успех, тоест да се хвърли 6ца
pgeon(10 - 1, 1/6)

# б)
# търси се P(X >= 6), a то се намира 1 - P(X <= 5)
1 - pgeon(5 - 1, 1/6)

