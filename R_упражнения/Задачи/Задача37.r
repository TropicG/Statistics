# Задача 37. За n = 200 и n = 1000 генерирайте данни x_1, x_2, ..., x_n от нормално разпределение с параметри mu = 4, sigma = 1.2.
# Начертайте емпиричната функция на разпределение на данните.
# На същата картинка добавете функцията на разпределени на случайна величина X ~ N(mu = 4, sigma = 1.2).

n <- 1000
mu <- 4
sigma <- 1.2

x <- rnorm(n, mu, sigma)
plot.ecdf(x, do.points=F)
curve(pnorm(x, mu, sigma), add=T, col="red")