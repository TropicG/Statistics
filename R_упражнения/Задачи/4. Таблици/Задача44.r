# Задача 44. Генерирайте променливи v1, ..., v5 като използвате следния код:
#
# v1 <- rep(4, 30)
# v2 <- rep(c(3.5,4.5), 15)
# v3 <- rep(c(3,5), 15)
# v4 <- rep(c(2:6), 6)
# v5 <- rep(c(2,6), 15)
#
# Представете графично всяка от променливите; намерете медианата, средната стойност 
# и стандартното отклонение.

my.summary <- function(x) {
  # Изчисляваме медиана, средно и стандартно отклонение за x и ги събираме в един вектор (na.rm=T игнорира липсващи стойности)
  res <- c( median(x, na.rm=T), mean(x, na.rm=T), sd(x, na.rm=T) )
  
  # Задаваме имена на трите позиции във вектора, за да се вижда коя стойност какво означава
  names(res) <- c("Median", "Mean", "StDev")

  res
}

v1 <- rep(4, 30)
v2 <- rep(c(3.5, 4.5), 15)
v3 <- rep(c(3, 5), 15)
v4 <- rep(c(2:6), 6)
v5 <- rep(c(2, 6), 15)

my.summary(v1)
my.summary(v2)
my.summary(v3)
my.summary(v4)
my.summary(v5)

